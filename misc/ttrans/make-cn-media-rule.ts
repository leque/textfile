import { addZhToneMark, printRule, RuleEntry }  from './util.ts'

const makeCnMediaRule = (): Array<RuleEntry> => {
    const result = cnMediaPats.flatMap(([pat, replacement]) => {
        const ps = addZhToneMark(pat);
        ps.push(pat);
        return ps.map((pattern) => ({ pattern, replacement }));
    });
    result.push({
        pattern: " ",
        replacement: ""
    });
    return result;
}

